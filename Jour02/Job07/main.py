def what_kind_of_dev(langage):
    if langage == "javascript":
        print("Tu est developpeur web")
    elif langage == "python":
        print("Tu est developpeur IA")
    elif langage == "java":
        print("Tu est developpeur logiciel")
    elif langage == "reactjs":
        print("Tu est developpeur mobile")
    else:
        print("Un jour, je serais le meilleur developpeur")


what_kind_of_dev("javascript")
what_kind_of_dev("python")
what_kind_of_dev("java")
what_kind_of_dev("reactjs")
what_kind_of_dev("C++")